package rename_with_inheritance.rr;

public class HasNoSourceException extends Exception {
	/**
	 * generated
	 */
	private static final long serialVersionUID = 1L;

	public HasNoSourceException() {
		super();
	}

	public HasNoSourceException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public HasNoSourceException(String message, Throwable cause) {
		super(message, cause);
	}

	public HasNoSourceException(String message) {
		super(message);
	}

	public HasNoSourceException(Throwable cause) {
		super(cause);
	}
}
